package Bean;

/**
 * @author Vitor Santos
 */
public class AmbientesBean {

    private int id;
    private String nome;
    private int idbloco;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdbloco() {
        return idbloco;
    }

    public void setIdbloco(int idbloco) {
        this.idbloco = idbloco;
    }

    
}
